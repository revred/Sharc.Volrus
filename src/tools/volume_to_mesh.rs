//! Extract an isosurface from a level-set grid using Marching Cubes.
//!
//! Standard 256-entry lookup tables (Lorensen & Cline / Paul Bourke).

use crate::grid::Grid;
use crate::math::Coord;
use crate::tree::LEAF_LOG2DIM;
use std::collections::HashMap;

/// Triangle mesh output.
pub struct Mesh {
    pub vertices: Vec<[f64; 3]>,
    pub triangles: Vec<[u32; 3]>,
}

impl Mesh {
    /// Number of triangles.
    pub fn tri_count(&self) -> usize {
        self.triangles.len()
    }

    /// Number of vertices.
    pub fn vertex_count(&self) -> usize {
        self.vertices.len()
    }

    /// Check if the mesh is watertight (every edge shared by exactly 2 triangles).
    pub fn is_watertight(&self) -> bool {
        let mut edge_counts: HashMap<(u32, u32), u32> = HashMap::new();
        for tri in &self.triangles {
            for i in 0..3 {
                let a = tri[i];
                let b = tri[(i + 1) % 3];
                let key = if a < b { (a, b) } else { (b, a) };
                *edge_counts.entry(key).or_insert(0) += 1;
            }
        }
        edge_counts.values().all(|&c| c == 2)
    }
}

/// Extract the zero-crossing isosurface using Marching Cubes.
///
/// Iterates over all leaf-adjacent 2x2x2 cells, classifies each cell
/// against `isovalue`, and emits triangles with edge-interpolated vertices.
pub fn volume_to_mesh(grid: &Grid<f32>, isovalue: f32) -> Mesh {
    let mut vertices: Vec<[f64; 3]> = Vec::new();
    let mut triangles: Vec<[u32; 3]> = Vec::new();
    // Edge vertex cache: (coord_a, coord_b) → vertex index.
    let mut edge_cache: HashMap<(Coord, Coord), u32> = HashMap::new();

    let dim = 1i32 << LEAF_LOG2DIM;
    let voxel_size = grid.transform().voxel_size;

    // Collect all leaf origins and also consider neighbours (cells at
    // leaf boundaries need access to adjacent leaves).
    let origins = grid.tree().leaf_origins();
    let mut cell_origins: Vec<Coord> = Vec::new();
    for &origin in &origins {
        cell_origins.push(origin);
        // Also consider the 6 face-adjacent leaf tiles (they share boundary cells).
        for &delta in &[
            Coord::new(-dim, 0, 0),
            Coord::new(dim, 0, 0),
            Coord::new(0, -dim, 0),
            Coord::new(0, dim, 0),
            Coord::new(0, 0, -dim),
            Coord::new(0, 0, dim),
        ] {
            cell_origins.push(origin + delta);
        }
    }
    cell_origins.sort();
    cell_origins.dedup();

    for &origin in &cell_origins {
        // Iterate over all 2x2x2 cells within this leaf tile.
        // A cell at (i,j,k) uses corners (i,j,k)..(i+1,j+1,k+1).
        for lx in 0..dim {
            for ly in 0..dim {
                for lz in 0..dim {
                    let base = origin + Coord::new(lx, ly, lz);
                    process_cell(
                        grid,
                        base,
                        isovalue,
                        voxel_size,
                        &mut vertices,
                        &mut triangles,
                        &mut edge_cache,
                    );
                }
            }
        }
    }

    Mesh {
        vertices,
        triangles,
    }
}

/// Process a single 2x2x2 marching-cubes cell.
fn process_cell(
    grid: &Grid<f32>,
    base: Coord,
    isovalue: f32,
    voxel_size: f64,
    vertices: &mut Vec<[f64; 3]>,
    triangles: &mut Vec<[u32; 3]>,
    edge_cache: &mut HashMap<(Coord, Coord), u32>,
) {
    // 8 corners in the standard MC ordering:
    // 0:(0,0,0) 1:(1,0,0) 2:(1,1,0) 3:(0,1,0)
    // 4:(0,0,1) 5:(1,0,1) 6:(1,1,1) 7:(0,1,1)
    let corners: [Coord; 8] = [
        base + Coord::new(0, 0, 0),
        base + Coord::new(1, 0, 0),
        base + Coord::new(1, 1, 0),
        base + Coord::new(0, 1, 0),
        base + Coord::new(0, 0, 1),
        base + Coord::new(1, 0, 1),
        base + Coord::new(1, 1, 1),
        base + Coord::new(0, 1, 1),
    ];

    let vals: [f32; 8] = [
        grid.get(corners[0]),
        grid.get(corners[1]),
        grid.get(corners[2]),
        grid.get(corners[3]),
        grid.get(corners[4]),
        grid.get(corners[5]),
        grid.get(corners[6]),
        grid.get(corners[7]),
    ];

    // Build case index.
    let mut cube_index: u8 = 0;
    for i in 0..8 {
        if vals[i] < isovalue {
            cube_index |= 1 << i;
        }
    }

    let edge_flags = EDGE_TABLE[cube_index as usize];
    if edge_flags == 0 {
        return; // No intersection.
    }

    // For each of the 12 edges, compute the interpolated vertex if needed.
    let mut edge_verts: [u32; 12] = [0; 12];
    for e in 0..12 {
        if edge_flags & (1 << e) != 0 {
            let (a, b) = EDGE_ENDPOINTS[e];
            let key = if corners[a] < corners[b] {
                (corners[a], corners[b])
            } else {
                (corners[b], corners[a])
            };

            edge_verts[e] = *edge_cache.entry(key).or_insert_with(|| {
                let t = if (vals[b] - vals[a]).abs() > 1e-10 {
                    ((isovalue - vals[a]) / (vals[b] - vals[a])) as f64
                } else {
                    0.5
                };
                let pa = [
                    corners[a].x as f64 * voxel_size,
                    corners[a].y as f64 * voxel_size,
                    corners[a].z as f64 * voxel_size,
                ];
                let pb = [
                    corners[b].x as f64 * voxel_size,
                    corners[b].y as f64 * voxel_size,
                    corners[b].z as f64 * voxel_size,
                ];
                let pos = [
                    pa[0] + t * (pb[0] - pa[0]),
                    pa[1] + t * (pb[1] - pa[1]),
                    pa[2] + t * (pb[2] - pa[2]),
                ];
                let idx = vertices.len() as u32;
                vertices.push(pos);
                idx
            });
        }
    }

    // Emit triangles from the triangle table.
    let tri_entry = &TRI_TABLE[cube_index as usize];
    let mut t = 0;
    while t < 15 && tri_entry[t] != -1 {
        triangles.push([
            edge_verts[tri_entry[t] as usize],
            edge_verts[tri_entry[t + 1] as usize],
            edge_verts[tri_entry[t + 2] as usize],
        ]);
        t += 3;
    }
}

